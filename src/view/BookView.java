    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        int option = JOptionPane.showConfirmDialog(this, "Confirma exclusão?", "Exclusão", JOptionPane.YES_NO_OPTION);
        
        if(option == JOptionPane.YES_OPTION) {
            int id = (Integer) bookTable.getValueAt(bookTable.getSelectedRow(), 0);
            int edition = (Integer) bookTable.getValueAt(bookTable.getSelectedRow(), 2);
            
            controller.deleteBook(id, edition);
            
            // atualiza tela
            ((DefaultTableModel)bookTable.getModel()).removeRow(bookTable.getSelectedRow());
            refreshScreen();
            JOptionPane.showMessageDialog(null, "Livro deletado conforme solicitado!");
        }
    }//GEN-LAST:event_deleteButtonActionPerformed

    public void refreshScreen() {
        int row = bookTable.getSelectedRow();
        if(row != -1) {
            deleteButton.setEnabled(true);
            titleTextField.setText(bookTable.getValueAt(row, 1).toString());
            editionTextField.setText(bookTable.getValueAt(row, 2).toString());
            yearTextField.setText(bookTable.getValueAt(row, 3).toString());
            authorTextArea.setText(bookTable.getValueAt(row, 4).toString()); 
        } else {
            deleteButton.setEnabled(false);
            titleTextField.setText("");
            editionTextField.setText("");
            yearTextField.setText("");
            authorTextArea.setText("");
        }   
    }
    
