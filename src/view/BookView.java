    public void fillBookTable(ArrayList<BookVO> bookList) {
        DefaultTableModel model = (DefaultTableModel) bookTable.getModel();
        model.setNumRows(0);
        
        for(BookVO b : bookList) {     
            String str = "";
            for(AuthorVO a : b.getAuthors()) {
                str += a.getName() + ", ";
            }
            str = str.substring(0, str.length()-2);
            
            model.addRow(new Object[] {
                b.getId(),
                b.getTitle(),
                b.getEdition(),
                b.getYear(),
                str
            });
        }
    }
    
    public void fillAuthorComboBox(ArrayList<AuthorVO> authorList) {
        for(AuthorVO a : authorList) {
            authorComboBox.addItem(a.getName());
        }
    }
    
